package jspbook.member;
public class Member {
	private int id;
	private String name;
	private int age;
	private String gender;
	private String address;
	private String phone;
	private String birthDay;
	private String email;
	private String job;
	private String registerDay;
	private String registerPeriod;
	private String registerEndDate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getBirthDay() {
		return birthDay;
	}
	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getRegisterDay() {
		return registerDay;
	}
	public void setRegisterDay(String registerDay) {
		this.registerDay = registerDay;
	}
	public String getRegisterPeriod() {
		return registerPeriod;
	}
	public void setRegisterPeriod(String registerPeriod) {
		this.registerPeriod = registerPeriod;
	}
	public String getRegisterEndDate() {
		return registerEndDate;
	}
	public void setRegisterEndDate(String registerEndDate) {
		this.registerEndDate = registerEndDate;
	}
}
